#include "fractol.h"

int		get_color(int i, int max_iter)
{
	int		r;
	int		g;
	int		b;
	double	t;

	t = (double)i / max_iter;
	r = (int)(9 * (1 - t) * t * t * t * 255);
	g = (int)(15 * (1 - t) * (1 - t) * t * t * 255);
	b = (int)(8.5 * (1 - t) * (1 - t) * (1 - t) * t * 255);
	return ((r << 16) + (g << 8) + b);
}
